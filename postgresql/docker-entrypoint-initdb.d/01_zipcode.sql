CREATE TABLE zipcode (
  jiscode VARCHAR(5),
  zipcode_old CHAR(5),
  zipcode CHAR(7),
  prefecture_kana VARCHAR(15),
  city_kana VARCHAR(31),
  street_kana VARCHAR(255),
  prefecture VARCHAR(15),
  city VARCHAR(31),
  street VARCHAR(255),
  flag1 SMALLINT,
  flag2 SMALLINT,
  flag3 SMALLINT,
  flag4 SMALLINT,
  flag5 SMALLINT,
  flag6 SMALLINT
);
